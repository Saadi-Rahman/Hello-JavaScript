
// "My mind is not feeling well today!!" show this line 39 times as output.

// using while loop
var myLine = 1;

while(myLine <= 39) {
    console.log("My mind is not feeling well today!! #" + myLine);
    myLine++;
}

console.log("-------------------------------------");

// using for loop
for(i = 1; i <= 39; i++){
    console.log("My mind is not feeling well today!! #" + i);
}