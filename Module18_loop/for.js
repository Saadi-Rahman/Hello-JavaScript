// while loop 
var roastGiven = 0;

while(roastGiven < 7){
    console.log("plz, give me a roast!");
    roastGiven++;
}

// for loop 
for(var roastGiven = 0; roastGiven < 7; roastGiven++) {
    console.log("plz, give some rice also.");
}

// simple verson of for loop
console.log("simple for loop");
for(var i = 0; i < 7; i++) {
    console.log("give me a glass of water!");
}


